#include "libft.h"

int		ft_count(const char *s, int c)
{
	int		i;
	int		count;

	i = 0;
	count = 0;
	while (s[i++] != '\0')
		if (s[i] != c)
		{
			count++;
			while (s[i] != c && s[i] != '\0')
				i++;
		}
	return (count);
}
