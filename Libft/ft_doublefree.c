#include "libft.h"

void				ft_doublefree(char ***tab)
{
	int				i;

	i = 0;
	if (tab && *tab)
		while (*tab[i] != NULL)
		{
			free(*tab[i]);
			tab[i] = NULL;
			i++;
		}
}
