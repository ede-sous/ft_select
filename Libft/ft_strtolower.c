#include "libft.h"

char		*ft_strtolower(char *s)
{
	size_t		i;

	if (s == NULL)
		return (NULL);
	i = -1;
	while (s[++i] != '\0')
		if (s[i] >= 'A' && s[i] <= 'Z')
			s[i] += 32;
	return (s);
}
