#include "libft.h"

void	ft_putnbrl(int n)
{
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else if (n > 2147483647)
		write(1, "Too big", 7);
	else
		ft_putstr(ft_itoa(n));
	ft_putchar('\n');
}
