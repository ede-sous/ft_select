#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	if (s != NULL && f != NULL)
	{
		i = -1;
		while (s[++i] != '\0')
			(*f)(i, &s[i]);
	}
	return ;
}
